class Event {
  final String title;
  final String url;
  final String organizer;
  final String location;
  final String targetGroup;
  final String dateTime;
  final String cost;

  Event({
    required this.title,
    required this.url,
    required this.organizer,
    required this.location,
    required this.targetGroup,
    required this.dateTime,
    required this.cost,
  });
}
